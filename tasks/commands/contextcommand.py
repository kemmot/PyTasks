
# to create contexts
# task context define <name> <context tags, attributes, etc>
# e.g. task context define work +work
# this sets a line in ini file like the following:
# context.work = +work

# to set contexts
# task context <name>
# e.g. task context work
# sets the following in the ini file
# context = work

# to unset contexts
# task context none
